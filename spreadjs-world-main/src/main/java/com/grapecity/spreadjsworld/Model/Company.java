package com.grapecity.spreadjsworld.Model;

public class Company {
    public String name ;
    public String logo ;
    public String slogan ;
    public String address ;
    public String city ;
    public String phone ;
    public String email ;


    public Company(String name,String logo,String slogan,String address,String city,String phone,String email){
        this.name = name;
        this.logo = logo;
        this.slogan = slogan;
        this.address = address;
        this.city = city;
        this.phone = phone;
        this.email = email;
    }
}
